import mongoose from "mongoose";

const userSchema:any = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    phone: {
        type: Number,
        required: true
    }
});

const Users = mongoose.model("users", userSchema);

export { Users };